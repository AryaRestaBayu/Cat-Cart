class Product {
  final int id;
  final String name;
  final String category;
  final int price;
  final String description;
  final String imageUrl;
  final List<String> listImage;

  Product({
    required this.id,
    required this.name,
    required this.category,
    required this.price,
    required this.description,
    required this.imageUrl,
    required this.listImage,
  });
}
